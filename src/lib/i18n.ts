export function detectLocale(): Locale {
  const navLang = navigator.language?.slice(0, 2).toLowerCase() ?? ''
  if (['vi', 'en', 'ko', 'de', 'fr', 'ja'].includes(navLang)) return navLang as Locale
  return 'vi'
}


